el yang pembangunan tembok Yerusalem dan Kaabah Zerubabel adalah dibawah tangan Nehemya. Sebab tatkala umat Israel keluar dari Babel maka nabi Daniel dipanggil Tuhan. Kitab Daniel ini mempunyai hubungan erat dengan kitab Wahyu oleh karena itu 70 sabat ini menyangkut pada zaman antiKris tetapi pembagiannya memerlukan hikmat dari Allah dan pengwahyuan sebab 70 sabat ini jika tidak mengerti pembagiannya, maka 70 sabat ini hanya berakhir sesudah Tuhan Yesus disalib.</p>
