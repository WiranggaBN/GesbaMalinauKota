            <p>Kolam Pembasuhan terbuat daripada tembaga dan punya kaki daripada tembaga. Letaknya antara Mezbah Korban Bakaran dan Pintu Kemah terbuat daripada kaca orang Israel yang mereka serahkan untuk pembuatan Kolam Pembasuhan Keluaran 38:8 yang berbunyi : “Dan lagi perbuatkanlah kolam tembaga serta dengan kakinya tembaga daripada segala cermin yang dibawa oleh perempuan  beramai-ramai kepada Pintu Kemah”.

                Kolam Pembasuhan adalah merupakan alat penghabisan yang ada dihalaman dimana Imam-imam Besar harus menyucikan dirinya membasuh tangannya dan kaki sebelum masuk didalam kemah.
                
                ARTI ROHANI :
                
                Kolam Pembasuhan berbicara tentang Baptisan Air. Oleh karena itu Baptisan Air adalah hal yang sangat penting sebagai penyucian yang terakhir untuk masuk dalam Kamar Suci. Baptisan Air adalah pekerjaan terakhir dari ketebusan atau pendahuluan dari penyempurnaan untuk terima kepenuhan Roh Suci.
                
                Penyucian dalam Kolam Pembasuhan adalah kewajiban bagi Imam-imam Besar agar tidak terbunuh. Keluaran 30:20 yang berbunyi :
                
                “Maka apabila mereka itu masuk kedalam kemah perhimpunan, tak akan jangan dibasuhkannya dirinya dengan air, asal jangan mereka itu mati dibunuh; maka demikianpun apabila mereka itu menghampiri mezbah hendak berbuat bakti dan memasang korban bakaran itu bagi Tuhan”.
                
                Oleh karena itu Baptisan Air merupakan suatu kewajiban yang penting yang harus dilakukan oleh setiap orang yang menerima Kristus sebagai Tuhan dan penebusnya. Yahya Pembabtis menamakan Baptisan Air melarikan diri dari murka Allah Matius 3:7 yang berbunyi : “Tetapi apabila dilihatnya banyak daripada orang parisi dan sadukipun datang minta dibaptiskan, berkatalah ia kepada mereka itu; Hai bangsa ular siapakah mengajar kamu melarikan dirimu daripada murka yang datang kelak?”
                
                Orang yang tidak dibaptiskan dengan air nanti kena murka Allah dan mati untuk selama-lamanya. Kalau umat Israel adalah umat pilihan Allah diharuskan untuk dibabtis apalagi kita orang kafir. Kita merupakan pohon zaitun hutan yang ditempelkan kepada pohon zaitun yang sebenarnya. Rum 11:16-21. Ingat kalau pohon zaitun yang sebenarnya tidak disayang tentu pohon zaitun hutan tidak ada gunanya (orang kafir).
                
                Pohon Zaitun – pohon zaitun yang sebenarnya yaitu umat Israel.
                
                Pohon Zaitun hutan             – orang kafir (kita) yang ditempelkan
                
                Itu sebabnya untuk mengenapi syarat kebenaran Allah  maka Kristus dibaptiskan di Sungai Yarden. Hendaklah diketahui kalau mempelai laki-laki itu dibaptiskan, maka mempelai perempuan tentu akan dibaptis juga. Sebagaimana istri tunduk kepada suami demikian juga sidang jemaat tunduk kepada Tuhan.
                
                Baptisan Air adalah baptisan yang kelihatan dan menerima Tuhan Yesus menjadi pengakuan yang nyata kepada umum bahwa seorang yang telah menerima Tuhan Yesus menjadi Juruselamat kita. Ini kesaksian tentang apa yang terjadi dalam dirinya sehingga umum mengetahui bahwa ia telah menerima Tuhan. Apabila yang menerima Tuhan Yesus Kristus dan mengakui pengorbanannya tanpa dibaptis berarti menutup jalan pada kesempurnaan. Ada kalanya setelah orang menerima Tuhan Yesus sebagai Juruselamat dan kemudian mendapat Baptisan Roh dari sorga ini adalah suatu anugerah yang besar tetapi jangan dia meremehkan Baptisan Air karena Baptisan Air perlu dilakukannya sebab itulah kesaksian Kisah Rasul 10:44-48.</p>
                <p>Baptisan Air ini dilakukan oleh Hamba-hamba Tuhan dan dilakukan dimana saja dan tidak memilih tempat tapi dimana saja ada air banyak yang dapat dipakai disitu bisa dibaptiskan orang. Adalah Hamba-hamba Tuhan yang sudah dibaptis juga tapi yang layak untuk melakukan baptisan air dapat dilakukan disetiap waktu tidak perlu tunggu pada hari-hari yang istimewa. Setelah oknum itu menyerah dikaki Tuhan diwaktu itu sebagai anak yang telah dilahirkan tidak memilih waktu demikian juga Baptisan Air.</p>
                <p>Dalam ayat ini memakai nama Bapa, Anak dan Rohulkudus dimana dinyatakan barang siapa yang percaya dan dibaptis dia akan selamat. Inilah komando Kristus yang tak bisa diabaikan harus dilakukan oleh setiap orang yang menerima Kristus sebagai Juruselamat akan tetapi dalam Kisah Rasul 2:37-38 murid-murid membaptiskan dengan nama Tuhan Yesus. Didalam Kisah Rasul 8:12 yang berbunyi : “Tetapi tatkala mereka itu percaya akan Pilipus yang memberitakan kabar kesukaan dari hal kerajaan Allah dan nama Yesus Kristus itu, maka mereka itu sekalian, baik laki-laki, baik perempuan, berbaptislah”.</p>
                <p>Tidak ada nama lain dibawah langit selain dari nama Tuhan Yesus Kristus. Itu sebabnya dalam Kisah Rasul 19:1-6 ada baptisan ulangan, oleh karena itu pada hari Pentakosta orang yang mendengar tatkala murid-murid itu dibaptis dengan Rohulkudus maka mereka bertanya : Apakah yang wajib kami perbuat, maka jawab Petrus dalam Kisah Rasul 2:37-38</p>
